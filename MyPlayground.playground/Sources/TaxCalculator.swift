import Foundation

public extension UInt {
    func roundedDown(toMultipleOf m: Self) -> Self {
        return self - (self % m)
    }
}

public extension Double {
    mutating func roundDownToTenCents() {
        self = (self * 10).rounded(.down) / 10
    }
}

public enum TaxClass: Int {
    case class1 = 1, class1A, class2, individual
}

public extension Taxes {
    
    // Contribution au fonds pour l'emploi pour les salaires supérieurs ou égaux à 9 040,00 euros.
    private func addContribution(tax: inout Double) {
        
        // 1) I = Impôt calculé arrondi au multiple inférieur de 0,1 €.
        tax.roundDownToTenCents()
        
        // Formule de la contribution au fonds pour l'emploi pour les salaires mensuels supérieurs à 12 585,00 € (classes 1 et 1a) respectivement 25 085,00 € (classe 2)
        if roundedSalary >= 12590 && taxClass == .class1 {
            tax += 0.09 * tax - 81.010
        } else if roundedSalary >= 12590 && taxClass == .class1A {
            tax += 0.09 * tax - 79.832
        } else if roundedSalary >= 25090 && taxClass == .class2 {
            tax += 0.09 * tax - 162.022
        
        // Pour un salaire mensuel inférieur ou égal à 12 585,00 € en classes 1 et 1a et 25 085,00 € en classe 2 le montant de l'impôt est (...) multiplié (...) par 1,07.
        } else {
            tax *= 1.07
        }
        
        // 2) La contribution au fonds pour l'emploi est arrondie au multiple inférieur de 0,1 €.
        tax.roundDownToTenCents()
    }
    
    func getTax() -> Double {
        
        var tax: Double = 0
        
        if taxableIncome < 1040 {
            fatalError("Error, taxable income lower than minimum income")
        }
        
        if taxClass == .individual {
            if individualRate != nil {
                return individualRate! * roundedSalary
            } else {
                fatalError("Individual tax rate not available")
            }
        }
        
        // Formules de l'impôt pour les salaires mensuels égaux ou supérieurs à 9 040,00 euros.
        if taxableIncome >= 9040 {
            switch taxClass {
            case .class1: do {
                    if roundedSalary <= 12585 {
                        tax = 0.40 * roundedSalary - 983.4275
                    } else if roundedSalary <= 16750 {
                        tax = 0.41 * roundedSalary - 1109.2775
                    } else {
                        tax = 0.42 * roundedSalary - 1276.7975
                    }
                }
            case .class1A: do {
                    if roundedSalary <= 12585 {
                        tax = 0.40 * roundedSalary - 1042.4
                    } else if roundedSalary <= 16750 {
                        tax = 0.41 * roundedSalary - 1168.25
                    } else {
                        tax = 0.42 * roundedSalary - 1335.77
                    }
                }
            case .class2: do {
                    if roundedSalary <= 16750 {
                        tax = 0.39 * roundedSalary - 1765.335
                    } else if roundedSalary <= 25085 {
                        tax = 0.40 * roundedSalary - 1932.855
                    } else if roundedSalary <= 33415 {
                        tax = 0.41 * roundedSalary - 2183.705
                    } else {
                        tax = 0.42 * roundedSalary - 2517.895
                    }
                }
            default: break
            }
            
            addContribution(tax: &tax)
            
        // Tableau des barèmes pour les salaires inférieurs à 9 040,00 euros.
        // Remarque: Les cotes d’impôt du barème de la retenue mensuelle sur les salaires comprennent la majoration de respectivement 7% et 9% en faveur du fonds pour l’emploi.
        } else {
            if let i = taxes.firstIndex(where: { $0[0] == roundedSalary }) {
                tax = taxes[i][taxClass.rawValue]
            } else {
                fatalError("roundedSalary not found in table")
            }
        }
        
        return tax
    }
}

